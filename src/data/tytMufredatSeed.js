const n = (ad, c = []) => ({
  ad,
  cocuklar: c.length && typeof c[0] === 'string' ? c.map(a => ({ ad: a, cocuklar: [] })) : c,
});

function _duzlestir(list, parentId, seviye, prefix, sonuc) {
  list.forEach((dugum, i) => {
    const id = `${prefix}_${i}`;
    sonuc.push({ id, parentId, seviye, ad: dugum.ad, sira: i });
    if (dugum.cocuklar.length > 0) _duzlestir(dugum.cocuklar, id, seviye + 1, id, sonuc);
  });
}

export function agaciDuzlestir(tur, dersler) {
  const sonuc = [];
  dersler.forEach((ders, i) => {
    const id = `${tur}_${i}`;
    sonuc.push({ id, parentId: null, seviye: 1, ad: ders.ad, sira: i });
    if (ders.cocuklar.length > 0) _duzlestir(ders.cocuklar, id, 2, id, sonuc);
  });
  return sonuc;
}

